$packageArgs = @{
  packageName   = $env:ChocolateyPackageName
  unzipLocation = $toolsDir
  fileType      = 'exe'
  url           = $url
  softwareName  = 'Windsurf*'
  checksum      = '3D342F7BC3E59F8B90BD8DFBF9E1300132E69FA5D7B76DFA9121D2C76A30CAF0'
  checksumType  = 'sha256'
  silentArgs    = "/VERYSILENT"
